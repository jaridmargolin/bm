function usage {
  echo "Usage: bm use <directory>"
  echo
  echo "Arguments"
  echo "  directory"
  echo "    Directory containg scripts to utilze. By default the value of the"
  echo "    directory utilized is  \`./_bash\` (relative to cwd)."
}


# ------------------------------------------------------------------------------
# execute
# ------------------------------------------------------------------------------

if [[ "$1" = "-h" ]] || [[ "$1" = "--help" ]]; then
  usage; return
fi
